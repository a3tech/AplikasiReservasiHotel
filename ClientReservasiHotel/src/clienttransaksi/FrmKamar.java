    public void aturTableKamar(List<Kamar> listKamar){
        dtmKamar = (DefaultTableModel) tblKamar.getModel();
        sorter = new TableRowSorter<TableModel>(dtmKamar);
        dtmKamar.setRowCount(0);
        tblKamar.setRowSorter(sorter);
        for(int i = 0; i < listKamar.size(); i++){
   dtmKamar.addRow(new Object[]{listKamar.get(i).getId_kamar(),
       listKamar.get(i).getId_admin(),listKamar.get(i).getTipe_kamar(),
       listKamar.get(i).getTarif(), listKamar.get(i).getKetersediaan()});
        }
    }
    
    public void tampilText(){
        baris = tblKamar.getSelectedRow();
        if(baris >=0){
            txtNoKamar.setText(dtmKamar.getValueAt(baris, 0).toString());
            txtIDAdmin.setText(dtmKamar.getValueAt(baris, 1).toString());
            cmbTipe.setSelectedItem(dtmKamar.getValueAt(baris, 2).toString());
            txtTarif.setText(dtmKamar.getValueAt(baris, 3).toString());
            txtKetersediaan.setText(dtmKamar.getValueAt(baris, 4).toString());
        }
    }

