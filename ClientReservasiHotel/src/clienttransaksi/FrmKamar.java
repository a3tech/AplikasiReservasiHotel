    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
    if (proses.equalsIgnoreCase("INSERT")){
    Kamar kamar = new Kamar();
    kamar.setId_admin(txtIDAdmin.getText());
    kamar.setTipe_kamar(cmbTipe.getSelectedItem().toString());
    kamar.setTarif(Integer.parseInt(txtTarif.getText()));
    kamar.setKetersediaan(Integer.parseInt(txtKetersediaan.getText()));
    try{
    statusProses = rst.InsertKamar(kamar);
        }catch(RemoteException re){
        statusProses = false;
    Logger.getLogger(FrmKamar.class.getName()).log(Level.SEVERE, null, re);
        }
        if(statusProses){
    JOptionPane.showMessageDialog(rootPane, "Data Kamar Berhasil Disimpan",
                    "Info",JOptionPane.INFORMATION_MESSAGE);
        }else{
    JOptionPane.showMessageDialog(rootPane, "Data Kamar Gagal Disimpan",
                    "Error", JOptionPane.ERROR_MESSAGE);
        } 
        aturButton(true);
            
        }else{
   Kamar kamar = new Kamar();
   kamar.setId_kamar(txtNoKamar.getText());
   kamar.setId_admin(txtIDAdmin.getText());
   kamar.setTipe_kamar(cmbTipe.getSelectedItem().toString());
   kamar.setTarif(Integer.parseInt(txtTarif.getText()));
   kamar.setKetersediaan(Integer.parseInt(txtKetersediaan.getText()));
         try{
        statusProses = rst.UpdateKamar(kamar);
        }catch(RemoteException re){
        statusProses = false;
    Logger.getLogger(FrmKamar.class.getName()).log(Level.SEVERE, null, re);
        }
        if(statusProses){
    JOptionPane.showMessageDialog(rootPane, "Data Kamar Berhasil Diubah",
                    "Info",JOptionPane.INFORMATION_MESSAGE);
        }else{
    JOptionPane.showMessageDialog(rootPane, "Data Kamar Gagal Diubah",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
        aturButton(true);
        
        }
    }//GEN-LAST:event_btnSaveActionPerformed

